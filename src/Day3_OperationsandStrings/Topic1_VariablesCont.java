package Day3_OperationsandStrings;

public class Topic1_VariablesCont {
    public static void main(String[] args) {

//!!!PRIMITIVE VARIABLES:
        //1. boolean ==> holds 2 values, ture or false
// type name = value;
        boolean boo1=true;
        System.out.println(boo1);

        boolean boo2=false;
        System.out.println(boo2);


        //2. byte ==> holds the whole number in range of -128 to 127
        //type name=value;
        byte b1=10;
        System.out.println(b1);


        //3. char ==> holds the characters for instance c or R, or " or }
        //type name = value; (for char values we use ' '(single quotes)
        char aChar='h';
        System.out.println(aChar);

       //4. short ==> holds whole number in the range og -32768 to 32676
//datatype name=value;
        short sh1=-10000;
        System.out.println(sh1);


       // 5. int ==> holds whole number
int i1=120;
        System.out.println(i1);

        //6. long ==>holds the whole number
        long ln1=545467L;
        System.out.println(ln1);


        //7.float ==> holds decimal numbers up to 7 decimal digit
float fl1=89.78f;
        System.out.println(fl1);


        //8.double ==> holds decimal numbers up to 16 decimal digit
        double d1=4.657578;
        System.out.println(d1);

        // declaring multiple variables on the same line

        //data type name = value;
        int i4=70,i5=90,i6=98;
        double d4,d5,d6;
        d4=90.7898;
        System.out.println(i5);
        System.out.println(d4);

    }
}
