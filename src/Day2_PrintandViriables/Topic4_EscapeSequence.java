package Day2_PrintandViriables;

import java.util.Arrays;

public class Topic4_EscapeSequence {
    public static void main(String[] args) {
        // \t it is going to leave paragraph space on the console-TAB space

        System.out.println("This is the line and if I want to put parag space \tbfnhkfhg ");

        // \n it is going to move on new line on the console
        System.out.println("This is a code where I go to next \nline");

        // \" prints one double quote " one the console
        System.out.println("This is \"Tanya\" ");

        // \' prints single quote on the console
        System.out.println("This is \'cool\'");

        System.out.println("");
//  \t:paragraph space
//  \n: new line
//  \": prints double quote " on the console
//  \\: prints \ on the console
//  \': prints single quote
//  ' : prints single quote
    }

}
